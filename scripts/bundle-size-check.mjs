#!/usr/bin/env node
/**
 * Bundle-size budget checker.
 *
 * Reads .next/build-manifest.json + .next/app-build-manifest.json produced by
 * `next build`, sums up JS chunk sizes on disk, compares against the baselines
 * stored in scripts/bundle-size-baseline.json, and exits non-zero when any
 * budget is breached.
 *
 * Usage:
 *   node scripts/bundle-size-check.mjs [--update-baseline]
 *
 * Flags:
 *   --update-baseline  Write current sizes to baseline file and exit 0.
 */

import { readFileSync, existsSync, statSync, writeFileSync } from "node:fs";
import { join, resolve } from "node:path";

const ROOT = resolve(import.meta.dirname, "..");
const NEXT_DIR = join(ROOT, ".next");
const BASELINE_FILE = join(ROOT, "scripts", "bundle-size-baseline.json");

// How many bytes over baseline triggers a failure (5 % or 50 KB, whichever is
// larger). Tweak as the app grows.
const BUDGET_BYTES_ABSOLUTE = 50 * 1024; // 50 KB
const BUDGET_PERCENT = 0.05; // 5 %

const UPDATE_BASELINE = process.argv.includes("--update-baseline");

// ── helpers ──────────────────────────────────────────────────────────────────

function kb(bytes) {
  return (bytes / 1024).toFixed(1) + " KB";
}

function safeSize(filePath) {
  try {
    return statSync(filePath).size;
  } catch {
    return 0;
  }
}

// ── collect chunks ────────────────────────────────────────────────────────────

function loadManifest(name) {
  const p = join(NEXT_DIR, name);
  if (!existsSync(p)) return {};
  try {
    return JSON.parse(readFileSync(p, "utf-8"));
  } catch {
    return {};
  }
}

const buildManifest = loadManifest("build-manifest.json");
const appManifest = loadManifest("app-build-manifest.json");

// Collect all unique JS chunk paths referenced in either manifest.
const chunkPaths = new Set();

function extractPaths(obj) {
  if (!obj || typeof obj !== "object") return;
  for (const v of Object.values(obj)) {
    if (Array.isArray(v)) {
      v.filter((s) => typeof s === "string" && s.endsWith(".js")).forEach((s) =>
        chunkPaths.add(s)
      );
    } else if (typeof v === "object") {
      extractPaths(v);
    }
  }
}
extractPaths(buildManifest);
extractPaths(appManifest);

// Measure sizes of each chunk on disk.
const chunkSizes = {};
for (const rel of chunkPaths) {
  // Paths in the manifest are relative to /_next/ (e.g. "static/chunks/foo.js")
  const rel2 = rel.replace(/^\/_next\//, "");
  const abs = join(NEXT_DIR, rel2);
  const size = safeSize(abs);
  if (size > 0) {
    chunkSizes[rel2] = size;
  }
}

const totalBytes = Object.values(chunkSizes).reduce((a, b) => a + b, 0);

// ── update-baseline mode ──────────────────────────────────────────────────────

if (UPDATE_BASELINE) {
  const baseline = {
    _note:
      "Auto-generated by `node scripts/bundle-size-check.mjs --update-baseline`. Commit after each intentional size increase.",
    _updated: new Date().toISOString().slice(0, 10),
    totalBytes,
    chunks: chunkSizes,
  };
  writeFileSync(BASELINE_FILE, JSON.stringify(baseline, null, 2) + "\n");
  console.log(`✅ Baseline updated: ${kb(totalBytes)} total across ${Object.keys(chunkSizes).length} chunks`);
  process.exit(0);
}

// ── budget check ──────────────────────────────────────────────────────────────

if (!existsSync(BASELINE_FILE)) {
  console.warn(
    "⚠️  No baseline file found at scripts/bundle-size-baseline.json.\n" +
      "   Run `node scripts/bundle-size-check.mjs --update-baseline` after a\n" +
      "   successful build to create one, then commit it."
  );
  // Non-fatal on first run — let the build succeed so baseline can be created.
  process.exit(0);
}

const baseline = JSON.parse(readFileSync(BASELINE_FILE, "utf-8"));
const baselineTotal = baseline.totalBytes ?? 0;
const delta = totalBytes - baselineTotal;
const deltaPercent = baselineTotal > 0 ? (delta / baselineTotal) * 100 : 0;

const budget = Math.max(BUDGET_BYTES_ABSOLUTE, baselineTotal * BUDGET_PERCENT);
const exceeded = delta > budget;

// Per-chunk report (only changed chunks)
const baselineChunks = baseline.chunks ?? {};
const rows = [];

for (const [path, size] of Object.entries(chunkSizes)) {
  const prev = baselineChunks[path];
  if (prev === undefined) {
    rows.push({ path, prev: null, curr: size, diff: size });
  } else if (prev !== size) {
    rows.push({ path, prev, curr: size, diff: size - prev });
  }
}
// Deleted chunks
for (const path of Object.keys(baselineChunks)) {
  if (!chunkSizes[path]) {
    rows.push({ path, prev: baselineChunks[path], curr: null, diff: -baselineChunks[path] });
  }
}
rows.sort((a, b) => Math.abs(b.diff) - Math.abs(a.diff));

// ── output ────────────────────────────────────────────────────────────────────

console.log("\n📦 Bundle size report");
console.log("=".repeat(60));
console.log(`  Baseline : ${kb(baselineTotal)}`);
console.log(`  Current  : ${kb(totalBytes)}`);
console.log(`  Delta    : ${delta >= 0 ? "+" : ""}${kb(delta)} (${deltaPercent >= 0 ? "+" : ""}${deltaPercent.toFixed(1)}%)`);
console.log(`  Budget   : ±${kb(budget)}`);
console.log(exceeded ? `  Status   : ❌ EXCEEDED` : `  Status   : ✅ OK`);

if (rows.length > 0) {
  console.log("\nChanged chunks:");
  for (const r of rows.slice(0, 20)) {
    const prev = r.prev !== null ? kb(r.prev) : "new";
    const curr = r.curr !== null ? kb(r.curr) : "deleted";
    const sign = r.diff >= 0 ? "+" : "";
    console.log(`  ${sign}${kb(r.diff).padStart(10)}  ${r.path}  (${prev} → ${curr})`);
  }
  if (rows.length > 20) {
    console.log(`  … and ${rows.length - 20} more chunks`);
  }
}

// Emit a machine-readable summary for the PR-comment step.
const summary = {
  baselineKb: (baselineTotal / 1024).toFixed(1),
  currentKb: (totalBytes / 1024).toFixed(1),
  deltaKb: (delta / 1024).toFixed(1),
  deltaPercent: deltaPercent.toFixed(1),
  budgetKb: (budget / 1024).toFixed(1),
  exceeded,
  changedChunks: rows.length,
};

// Write JSON summary so the GH Actions step can read it.
const summaryPath = join(ROOT, "bundle-size-summary.json");
writeFileSync(summaryPath, JSON.stringify(summary, null, 2) + "\n");

if (exceeded) {
  console.error(
    `\n❌ Bundle size exceeded the budget by ${kb(delta - budget)}.\n` +
      `   If this is intentional, run:\n` +
      `   node scripts/bundle-size-check.mjs --update-baseline\n` +
      `   and commit the updated baseline.`
  );
  process.exit(1);
}
